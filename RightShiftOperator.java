class RightShiftOperator {
    public static void main(String args[]) {
        // Right Shift Operator (>>) shifts the bits of a number to the right.

        // 10 in binary: 1010
        // Shifting 2 bits to the right: 10 (in decimal, it's 2)
        System.out.println(10 >> 2);  // Prints 2

        // 20 in binary: 10100
        // Shifting 2 bits to the right: 101 (in decimal, it's 5)
        System.out.println(20 >> 2);  // Prints 5

        // 20 in binary: 10100
        // Shifting 3 bits to the right: 10 (in decimal, it's 2)
        System.out.println(20 >> 3);  // Prints 2
    }
}


